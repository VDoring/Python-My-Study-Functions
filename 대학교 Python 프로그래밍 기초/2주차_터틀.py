import turtle
import time

def td():
    time.sleep(0.7)

turtle.shape("turtle")
td()
turtle.write(turtle.position())
td()
turtle.forward(50)
td()
turtle.write(turtle.position())
td()
turtle.left(90)
td()
turtle.forward(50)
td()
turtle.write(turtle.position())
td()
# backward()도 있다.