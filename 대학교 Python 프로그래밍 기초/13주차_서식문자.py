a = 10
b = 3.546
ch = 'a'
st = 'abc'

print('value = %d' %a) # value = 10
print('value = %-4d,%4d' %(a,a)) # value = 10  ,  10
print('value = %f' %b) # value = 3.546000
print('value = %4.1f,%4.2f' %(b,b)) # value =  3.5,3.55
print('value = %c' %ch) # value = a
print('value = %s' %st) # value = abc